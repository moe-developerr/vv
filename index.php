	<script>
		var slider = (function ($w) {
			var debouncePromise;
			var $slider = $('.slider');
			var settings = {
				debounceTime: 100
			};
			activate();

			function activate()
			{
				resize();
				$w.resize(debounce);
			}

			function debounce()
			{
				clearTimeout(debouncePromise);
				debouncePromise = setTimeout(resize, settings.debounceTime);
			}

			function resize()
			{
				if($w.innerWidth() >= 768) $slider.innerHeight($w.innerHeight() - 220);
				else $slider.innerHeight('');
				$slider.data('royalSlider').updateSliderSize();
			}

		})($(window));
	</script>
